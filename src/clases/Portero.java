package clases;
public class Portero {
    private String idFutbolista;
    private int atajadas;
    private int golesEncajados;

    // Getters y setters
    public String getIdFutbolista() { return idFutbolista; }
    public void setIdFutbolista(String idFutbolista) { this.idFutbolista = idFutbolista; }
    public int getAtajadas() { return atajadas; }
    public void setAtajadas(int atajadas) { this.atajadas = atajadas; }
    public int getGolesEncajados() { return golesEncajados; }
    public void setGolesEncajados(int golesEncajados) { this.golesEncajados = golesEncajados; }
}