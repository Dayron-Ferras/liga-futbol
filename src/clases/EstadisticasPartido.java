package clases;
public class EstadisticasPartido {
    private String idPartido;
    private String idFutbolista;
    private int goles;
    private int asistencias;
 
    
	// Getters y setters
    public String getIdPartido() { return idPartido; }
    public void setIdPartido(String idPartido) { this.idPartido = idPartido; }
    public String getIdFutbolista() { return idFutbolista; }
    public void setIdFutbolista(String idFutbolista) { this.idFutbolista = idFutbolista; }
    public int getGoles() { return goles; }
    public void setGoles(int goles) { this.goles = goles; }
    public int getAsistencias() { return asistencias; }
    public void setAsistencias(int asistencias) { this.asistencias = asistencias; }
}