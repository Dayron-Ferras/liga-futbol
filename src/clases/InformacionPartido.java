package clases;
public class InformacionPartido {
    private String idPartido;
    private String equipoLocal;
    private String equipoVisitante;
    private int golesLocal;
    private int golesVisitante;
    private int asistenciasLocal;
    private int asistenciasVisitante;

    // Getters y setters
    public String getIdPartido() { return idPartido; }
    public void setIdPartido(String idPartido) { this.idPartido = idPartido; }
    public String getEquipoLocal() { return equipoLocal; }
    public void setEquipoLocal(String equipoLocal) { this.equipoLocal = equipoLocal; }
    public String getEquipoVisitante() { return equipoVisitante; }
    public void setEquipoVisitante(String equipoVisitante) { this.equipoVisitante = equipoVisitante; }
    public int getGolesLocal() { return golesLocal; }
    public void setGolesLocal(int golesLocal) { this.golesLocal = golesLocal; }
    public int getGolesVisitante() { return golesVisitante; }
    public void setGolesVisitante(int golesVisitante) { this.golesVisitante = golesVisitante; }
    public int getAsistenciasLocal() { return asistenciasLocal; }
    public void setAsistenciasLocal(int asistenciasLocal) { this.asistenciasLocal = asistenciasLocal; }
    public int getAsistenciasVisitante() { return asistenciasVisitante; }
    public void setAsistenciasVisitante(int asistenciasVisitante) { this.asistenciasVisitante = asistenciasVisitante; }
}