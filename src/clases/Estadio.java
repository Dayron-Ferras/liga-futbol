
package clases;
public class Estadio {
    private String nombre;
    private int capacidadMax;
    private int audienciaTotal;

    // Getters y setters
    public String getNombre() { return nombre; }
    public void setNombre(String nombre) { this.nombre = nombre; }
    public int getCapacidadMax() { return capacidadMax; }
    public void setCapacidadMax(int capacidadMax) { this.capacidadMax = capacidadMax; }
    public int getAudienciaTotal() { return audienciaTotal; }
    public void setAudienciaTotal(int audienciaTotal) { this.audienciaTotal = audienciaTotal; }
}